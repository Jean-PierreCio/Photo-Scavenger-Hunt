import UIKit
import CoreLocation

class Task {
    let title: String
    let description: String
    var image: UIImage?
    var imageLocation: CLLocation?
    var isComplete: Bool {
        image != nil
    }

    init(title: String, description: String) {
        self.title = title
        self.description = description
    }

    func set(_ image: UIImage, with location: CLLocation) {
        self.image = image
        self.imageLocation = location
    }
}

extension Task {
    static var mockedTasks: [Task] {
        return [
            Task(title: "Find a statue",
                 description: "It has to be famous"),
            Task(title: "Find a 4 leaf clover",
                 description: "no cheating"),
            Task(title: "Find a waterfall",
                 description: "if you want take a shower in the waterfall")
        ]
    }
}
